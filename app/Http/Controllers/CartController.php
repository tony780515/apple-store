     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, $id)
    {
        $cart = Product::find($id);

        Cart::create(array(
            'product_id' => $cart->id,
            'quantity' => 1
            ));

        return redirect('/shoppingcart');
    }

